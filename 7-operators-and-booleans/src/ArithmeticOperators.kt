fun main(args: Array<String>) {
    // Addition
    println(5 + 2)      // 7

    // Subtraktion
    println(5 - 2)      // 3

    // Multiplikation
    println(5 * 2)      // 10

    // Division (ganzzahlig)
    println(5 / 2)      // 2

    // Division (Gleitkommazahlen)
    println(5.0f / 2.0f)  // 2.5

    // Modulo
    println(5 % 2)      // 1

    // Inkrement/Dekrement
    var cats = 3
    println(cats++)     // 3
    println(cats)       // 4
    println(++cats)     // 5
    println(cats--)     // 5
    println(cats)       // 4
    println(--cats)     // 3


    // String-Konkatenation
    println("Hi" + " John")
}
